public class MinMaxNumbers
{
    public static void main(String[] args)
    {
        System.out.println("------------------------------------------------------");

        System.out.println("Тип byte.");
        System.out.println("Минимальное отрицательное значение: " + Byte.MIN_VALUE);
        System.out.println("Максимальное положительное значение: " + Byte.MAX_VALUE);
        System.out.println("------------------------------------------------------");

        System.out.println("Тип short.");
        System.out.println("Минимальное отрицательное значение: " + Short.MIN_VALUE);
        System.out.println("Максимальное положительное значение: " + Short.MAX_VALUE);
        System.out.println("------------------------------------------------------");

        System.out.println("Тип int.");
        System.out.println("Минимальное отрицательное значение: " + Integer.MIN_VALUE);
        System.out.println("Максимальное положительное значение: " + Integer.MAX_VALUE);
        System.out.println("------------------------------------------------------");

        System.out.println("Тип long.");
        System.out.println("Минимальное отрицательное значение: " + Long.MIN_VALUE);
        System.out.println("Максимальное положительное значение: " + Long.MAX_VALUE);
        System.out.println("------------------------------------------------------");

        System.out.println("Тип float.");
        System.out.println("Минимальное отрицательное значение: " + (-Float.MAX_VALUE));
        System.out.println("Максимальное положительное значение: " + Float.MAX_VALUE);
        System.out.println("------------------------------------------------------");

        System.out.println("Тип double.");
        System.out.println("Минимальное отрицательное значение: " + (-Double.MAX_VALUE));
        System.out.println("Максимальное положительное значение: " + Double.MAX_VALUE);
        System.out.println("------------------------------------------------------");

    }
}
